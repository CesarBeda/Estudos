// Operadores Bitwise

/* &, |, ^*/

let n1=11;
let n2=14;

let res = n1 & n2; // = a 10 em binário

console.log(res);

n1=10;
n2=11;

res = n1 | n2; // = a 11 em binário

console.log(res);

n1=13;
n2=14;

res = n1 ^ n2; // = a 3 em binário

console.log(res);

n1=25;

res = n1 << 1; // desloca 1 bit para esquerda

console.log(res);

n1=30;

res = n1 >> 1; // desloca 1 bit para esquerda

console.log(res);

