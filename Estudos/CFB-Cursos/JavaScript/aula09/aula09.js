
//Pré e incremento e pós incremento.

// valor++  incremento a direta pós incremento, primeiro usa valor depois incrementa
let n=10;

console.log('pós-incremento')
console.log(n);
console.log(n++); //primeiro exibe depois incrementa
console.log(n);


//++valor incremento a esquerda pré incremento, primeiro incrementa depois utiliza o valor
let x=20;

console.log('\npré-incremento')
console.log(x);
console.log(++x);//primeiro incrementa depois exibe
console.log(x);