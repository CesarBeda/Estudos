//OPERAÇÕES MATEMÁTICAS

Math.abs(10);	//CONVERTE O SINAL PARA POSITIVO
Math.abs(-10);

Math.ceil(1.1);	//ARREDONDA PARA CIMA - PROXIMO INTEIRO
Math.ceil(-1.1); 

Math.floor(9.9);//ARREDONDA  PARA BAIXO - PROXIMO INTEIRO
Math.floor(-9.9);

Math.round(4.5);	//ARREDONDA PARA CIMA OU PARA BAIXO EM 0.5
Math.round(4.4);
Math.round(-4.5);
Math.round(-4.6);

Math.sign(5);	// RETORNA 1 SE FOR POSITIVO E -1 SE NEGATIVO
Math.sign(-5);	// SERVE PRA SABER O SINAL DE UM NUMERO

Math.trunc(2.3);	//ELIMINA A PARTE DECIMAL DO NUMERO
Math.trunc(-2.3);	// MUITO UTIL PRA PEGAR A PARTE INTEIRA DO NUMERO
